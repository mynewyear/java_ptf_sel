package selenium.models;

public class DuckData {

    public String name = "";
    public String oldPrice = "";
    public String colorOldPrice = "";
    public String crossLineOldPrice = "";
    public String sizeOldPrice = "";
    public String newPrice = "";
    public String colorNewPrice = "";
    public String boldNewPrice = "";
    public String sizeNewPrice = "";

    public DuckData(String name,
                    String oldPrice,
                    String colorOldPrice,
                    String crossLineOldPrice,
                    String sizeOldPrice,
                    String newPrice,
                    String colorNewPrice,
                    String boldNewPrice,
                    String sizeNewPrice) {
        this.name = name;
        this.oldPrice = oldPrice;
        this.colorOldPrice = colorOldPrice;
        this.crossLineOldPrice = crossLineOldPrice;
        this.sizeOldPrice = sizeOldPrice;
        this.newPrice = newPrice;
        this.colorNewPrice = colorNewPrice;
        this.boldNewPrice = boldNewPrice;
        this.sizeNewPrice = sizeNewPrice;

    }

}
